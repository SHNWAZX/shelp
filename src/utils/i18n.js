function t(lang, key, vars = {}) {
  const langData = translations[lang] || translations['en'];
  let str = langData[key] || translations['en'][key] || key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
  }
  return str;
}

